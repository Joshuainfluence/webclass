print(type("influence")) # This will output: <class 'str'>
print(type(42))          # This will output: <class 'int'>
print(type(3.14))        # This will output: <class 'float'>
print(type(True))       # This will output: <class 'bool'>
print(type([1, 2, 3]))  # This will output: <class 'list'>
print(type((1, 2, 3)))  # This will output: <class 'tuple'>
print(type({'a': 1}))   # This will output: <class 'dict'>
print(type({1, 2, 3}))  # This will output: <class 'set'>
print(type(None))       # This will output: <class 'NoneType'>
# Demonstrating different data types in Python
print(type(b'bytes'))   # This will output: <class 'bytes'> 
print(type(bytearray(5))) # This will output: <class 'bytearray'>
print(type(frozenset([1, 2, 3]))) # This will output: <class 'frozenset'>
print(type(range(5)))   # This will output: <class 'range'>
print(type(complex(1, 2))) # This will output: <class 'complex'>
print(type(memoryview(b'hello'))) # This will output: <class 'memoryview'>
print(type(lambda x: x)) # This will output: <class 'function'>
print(type(Exception("error"))) # This will output: <class 'Exception'>
print(type(object()))   # This will output: <class 'object'>
